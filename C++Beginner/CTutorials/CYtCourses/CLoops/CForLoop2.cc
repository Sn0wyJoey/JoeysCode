#include <iostream>
using namespace std;



//lets use for loops to iterate through the contents of an array

int main(){
    int nums[] = {0, 1, 222, 3, 4, 5, 6, 77}; // makes a list of nums

    for(int i = 0; i < 8; i++){ //array indexes start at 0, so lets start the i variable at 0. also, while i is less than the number of elements in the array, we keep looping.
         cout << nums[i] << endl;
         //keeps looping through and printing the elements in the array.



    }





}