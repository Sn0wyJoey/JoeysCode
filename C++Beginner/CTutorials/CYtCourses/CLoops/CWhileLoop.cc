#include <iostream>
using namespace std;

int main(){
    int loopVariable = 1; //variable for loop, we set it to 1
    while(loopVariable <=10){ //as long as loopVariable <= 10, it will loop that. 
        cout << loopVariable << endl; //prints loopbariable
        loopVariable++; /*adds 1 to loopvariable
        so this prints loopVariable and adds 1 to it until it reaches 10 (because of the while loop) */
    }
}
