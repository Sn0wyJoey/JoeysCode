 #include <iostream>
using namespace std;

int main()
{
// char, represents one character.
    char variableName = 'A'; 

// string, a string of characters. 
    string variableName1 = "Joey's House";

// int, represents a WHOLE number.
    int variableName2 = 50;

// double, represents a decimal number.
// doubles store more decimal points than a float.
    double gpa = -4.5;

// float, represents a decimal number.
// floats store less decimal points than a double.
// floats are only used in specific circumstances, use doubles.
    float sum = 4.0;

// bool, a true or false value.(boolean)
    bool isTall = false;
    bool isMale = true;

// we can print these variables
    cout << variableName << endl;
    cout << "This is " << variableName1 << endl;
    cout << variableName2 << endl;
    cout << gpa << endl;
    cout << sum << endl;
    cout << isTall << endl;
    cout << isMale << endl;
    }