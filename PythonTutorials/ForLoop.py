#a for loop is a loop that will execute for a limited amount of time

for i in range(10): #for range(x), x is the amount of times it will loop. i will increase by 1 per loop.
    print(i + 1) #i will be 0, 1, 2, etc. i + 1 will be 1, 2, 3 etc. It will reach 10 because computers start at 0.

for j in range(50, 100 + 1): #prints from 50 to 100
    print(j)

for k in range(50, 100+1, 2): #the 3rd perameter counts by 2. Sort of like step
    print(k)

for l in "Joey Brar":
    print(l) #prints each letter of the name to a new line

